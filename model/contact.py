class Contact:

    def __init__(self, firstname=None, middlename=None, lastname=None, nickname=None, address=None, home=None, email=None, title=None, id=None):
        self.firstname = firstname
        self.middlename = middlename
        self.lastname = lastname
        self.nickname = nickname
        self.address = address
        self.home = home
        self.email = email
        self.title = title
        self.id = id
